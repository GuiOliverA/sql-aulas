--Existe um relação entre as tabelas (ENTIDADEs) dados e cursos?
--Sim, uma pessoa pode Assistir (RELACIONAMENTO) um curso


--Tipos de relacionamento:

--1 para 1 - Uma linha se relaciona com exatamente uma outra | Ex: Pessoa ↔ Passaporte
--1 para N - Uma linha se relaciona com várias outras | Ex: Cliente → Pedidos
--N para N - Várias linhas se relacionam com várias outras | Ex: Alunos ↔ Disciplinas

